// --- runCompareSearch: orchestration, with the git/process/usage-index dependencies faked. ---

test("runCompareSearch golden path: indexes, captures both snapshots, writes then removes .agents/eval-search.md, and scaffolds evals/<name>", async () => {
  const { dir: repoDir, cleanup } = await tempDir("tangent-compare-search-");
  try {
    const calls = { searchIndex: [], captures: [] };
    const result = await runCompareSearch({
      name: "my-comparison",
      repo: repoDir,
      promptFlags: { task: "add a --version flag" },
      now: new Date("2026-07-06T00:00:00.000Z")
    }, {
      /** Fake repo-root resolver: returns the temp repo without running git. */
      resolveRepoRoot: async () => repoDir,
      /** Fake search index runner: records that it was called instead of spawning `tangent`. */
      runSearchIndex: async (repoRoot) => { calls.searchIndex.push(repoRoot); },
      /** Fake context snapshot capture: records the call and returns a deterministic ref instead of touching git. */
      captureSnapshot: async (options) => {
        calls.captures.push(options);
        return fakeCaptureResult(options.name);
      }
    });

    assert.deepEqual(calls.searchIndex, [repoDir]);
    assert.equal(calls.captures.length, 2, "captures both the no-search and with-search snapshots");
    assert.equal(calls.captures[0].name, "my-comparison-no-search");
    assert.equal(calls.captures[0].includeDirtyContext, undefined, "the no-search capture must not allow dirty context files");
    assert.equal(calls.captures[1].name, "my-comparison-with-search");
    assert.equal(calls.captures[1].includeDirtyContext, true, "the with-search capture allows the newly written .agents file to be dirty");

    // .agents/eval-search.md must exist during the with-search capture and be gone afterward.
    assert.equal(await pathExists(path.join(repoDir, ".agents", "eval-search.md")), false, "the instruction file is deleted after the with-search capture");
    assert.equal(await pathExists(path.join(repoDir, ".agents")), false, "the .agents directory it created is also removed, restoring the tree");

    assert.equal(result.noSearchRef, "refs/tangent/eval/contexts/my-comparison-no-search");
    assert.equal(result.withSearchRef, "refs/tangent/eval/contexts/my-comparison-with-search");
    assert.equal(result.promptText, "add a --version flag");
    assert.equal(result.promptSourceLabel, "--task");

    const specPath = path.join(repoDir, "evals", "my-comparison", "eval.json");
    const promptPath = path.join(repoDir, "evals", "my-comparison", "prompts", "task.md");
    assert.equal(result.specPath, specPath);
    assert.equal(result.promptPath, promptPath);

    const spec = JSON.parse(await readFile(specPath, "utf8"));
    assert.equal(spec.name, "my-comparison");
    assert.deepEqual(spec.cases[0].variants, [
      { id: "no-search", context: { mode: "snapshot", ref: "refs/tangent/eval/contexts/my-comparison-no-search" } },
      { id: "with-search", context: { mode: "snapshot", ref: "refs/tangent/eval/contexts/my-comparison-with-search" } }
    ]);

    const taskPrompt = await readFile(promptPath, "utf8");
    assert.match(taskPrompt, /add a --version flag\n$/);
  } finally {
    await cleanup();
  }
});

