/** Creates a temp directory for a test, and returns it plus a cleanup function. */
async function tempDir(prefix) {
  const dir = await mkdtemp(path.join(tmpdir(), prefix));
  /** Removes the temp directory created for this test. */
  const cleanup = () => rm(dir, { recursive: true, force: true });
  return { dir, cleanup };
}

/** Returns whether a path exists, for test assertions. */
async function pathExists(targetPath) {
  return access(targetPath).then(() => true).catch(() => false);
}

/** Builds a minimal but schema-valid capture result for a fake `captureSnapshot` dependency. */
function fakeCaptureResult(name) {
  return {
    ref: `refs/tangent/eval/contexts/${name}`,
    commit: `commit-${name}`,
    manifest: {
      schema: "eval.context.v1",
      id: `ctx_${name}`,
      createdAt: "2026-07-06T00:00:00.000Z",
      source: { repoRoot: "/repo", cwd: ".", empty: false, dirtyContextIncluded: false },
      discovery: { cwd: ".", includeAncestors: true, patterns: [] },
      files: []
    }
  };
}

// --- Pure helpers: name defaulting, prompt-source precedence, substantive-message filtering. ---

test("defaultCompareSearchName builds search-compare-<yyyymmdd> from the given clock", () => {
  assert.equal(defaultCompareSearchName(new Date("2026-07-06T14:30:00.000Z")), "search-compare-20260706");
});

test("resolvePromptSource defaults to auto when no source flag is given", () => {
  assert.deepEqual(resolvePromptSource({}), { kind: "auto" });
});

test("resolvePromptSource resolves each single source flag", () => {
  assert.deepEqual(resolvePromptSource({ prompt: "task.md" }), { kind: "file", path: "task.md" });
  assert.deepEqual(resolvePromptSource({ task: "add a flag" }), { kind: "text", text: "add a flag" });
  assert.deepEqual(resolvePromptSource({ session: "abc123" }), { kind: "session", sessionId: "abc123" });
});

test("resolvePromptSource rejects more than one source flag", () => {
  assert.throws(() => resolvePromptSource({ prompt: "task.md", task: "add a flag" }), /at most one of --prompt, --task, or --session/);
  assert.throws(() => resolvePromptSource({ task: "x", session: "y" }), /at most one of/);
});

test("isSubstantiveUserMessage rejects empty, whitespace-only, and command-XML text", () => {
  assert.equal(isSubstantiveUserMessage("add a --version flag"), true);
  assert.equal(isSubstantiveUserMessage(""), false);
  assert.equal(isSubstantiveUserMessage("   "), false);
  assert.equal(isSubstantiveUserMessage("<command-name>mark</command-name>\n<command-message>note</command-message>"), false);
});

test("selectLastSubstantiveMessage picks the last non-command-XML message in ordinal order", () => {
  const messages = [
    { ordinal: 1, text: "first ask" },
    { ordinal: 5, text: "second ask" },
    { ordinal: 9, text: "<command-name>mark</command-name>" }
  ];
  assert.equal(selectLastSubstantiveMessage(messages), "second ask");
});

test("selectLastSubstantiveMessage returns undefined when no message is substantive", () => {
  assert.equal(selectLastSubstantiveMessage([{ ordinal: 1, text: "<command-name>x</command-name>" }, { ordinal: 2, text: "   " }]), undefined);
});

test("selectMostRecentSubstantiveMessage sorts by `at` descending and filters command-XML", () => {
  const candidates = [
    { text: "older ask", at: "2026-07-01T00:00:00.000Z" },
    { text: "<command-name>mark</command-name>", at: "2026-07-06T00:00:00.000Z" },
    { text: "newest substantive ask", at: "2026-07-05T00:00:00.000Z" }
  ];
  assert.equal(selectMostRecentSubstantiveMessage(candidates), "newest substantive ask");
});

test("truncateForDisplay collapses whitespace and cuts at maxLength with an ellipsis", () => {
  assert.equal(truncateForDisplay("hello   world", 20), "hello world");
  assert.equal(truncateForDisplay("a".repeat(210), 200), `${"a".repeat(199)}…`);
});

