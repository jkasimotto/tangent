test("runCompareSearch still deletes .agents/eval-search.md when the with-search capture fails", async () => {
  const { dir: repoDir, cleanup } = await tempDir("tangent-compare-search-capture-failure-");
  try {
    let captureCount = 0;
    await assert.rejects(
      runCompareSearch({
        name: "my-comparison",
        repo: repoDir,
        promptFlags: { task: "add a --version flag" }
      }, {
        /** Fake repo-root resolver: returns the temp repo without running git. */
        resolveRepoRoot: async () => repoDir,
        /** Fake search index runner: does nothing instead of spawning `tangent`. */
        runSearchIndex: async () => {},
        /** Fake context snapshot capture: succeeds for no-search, then fails the with-search capture to exercise cleanup. */
        captureSnapshot: async (options) => {
          captureCount += 1;
          if (captureCount === 2) throw new Error("simulated with-search capture failure");
          return fakeCaptureResult(options.name);
        }
      }),
      /simulated with-search capture failure/
    );

    assert.equal(await pathExists(path.join(repoDir, ".agents", "eval-search.md")), false, "the instruction file is still removed after a failed with-search capture");
  } finally {
    await cleanup();
  }
});

