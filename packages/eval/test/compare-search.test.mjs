test("runCompareSearch aborts before any side effect when .agents/eval-search.md already exists", async () => {
  const { dir: repoDir, cleanup } = await tempDir("tangent-compare-search-existing-file-");
  try {
    await mkdir(path.join(repoDir, ".agents"), { recursive: true });
    await writeFile(path.join(repoDir, ".agents", "eval-search.md"), "pre-existing content", "utf8");

    const calls = { searchIndex: 0, captures: 0 };
    await assert.rejects(
      runCompareSearch({
        name: "my-comparison",
        repo: repoDir,
        promptFlags: { task: "add a --version flag" }
      }, {
        /** Fake repo-root resolver: returns the temp repo without running git. */
        resolveRepoRoot: async () => repoDir,
        /** Fake search index runner: counts calls so the abort path can assert none happened. */
        runSearchIndex: async () => { calls.searchIndex += 1; },
        /** Fake context snapshot capture: counts calls so the abort path can assert none happened. */
        captureSnapshot: async (options) => { calls.captures += 1; return fakeCaptureResult(options.name); }
      }),
      /\.agents\/eval-search\.md already exists/
    );

    assert.equal(calls.searchIndex, 0, "must not index the repo once the pre-existing file is found");
    assert.equal(calls.captures, 0, "must not capture any snapshot once the pre-existing file is found");
    assert.equal(await readFile(path.join(repoDir, ".agents", "eval-search.md"), "utf8"), "pre-existing content", "the pre-existing file is left untouched");
  } finally {
    await cleanup();
  }
});

